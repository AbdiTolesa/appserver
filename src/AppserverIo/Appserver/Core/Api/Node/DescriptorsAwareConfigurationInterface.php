interface DescriptorsAwareConfigurationInterface
{

    /**
     * Array with the descriptors.
     *
     * @param array $descriptors The descriptors
     *
     * @return void
     */
    public function setDescriptors(array $descriptors);

    /**
     * Array with the descriptors.
     *
     * @return array
     */
    public function getDescriptors();
}
